# Ruby library for ongoing experimentation

module MyLib
  
  def self.hello
    puts "Hello from MyLib!"
  end

  def self.add(x, y)
    x + y
  end

  def self.subtract(x, y)
    x - y
  end
 
  def self.multiply(x, y)
    x * y
  end
 
  def self.divide(x, y)
    x / y
  end

end

